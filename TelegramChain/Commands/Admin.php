namespace TelegramNotifier\TelegramChain\Commands;


use TelegramNotifier\TelegramChain\Command;
use TelegramBot\Api\BotApi;
use TelegramNotifier\Helper;
use TelegramNotifier\TelegramDb;

class Admin extends Command
{
    protected $name = '/admin';

    protected $description = ' Welcome to admin-panel!';

    public function handle($data)
    {
        if ($data->getMessage()->getText() == $this->getName()) {
            $chatId = $data->getMessage()->getChat()->getId();
            $keyboard = $this->keyboard($chatId);
            TelegramDb::updateStatus($chatId, 'admin');
            $this->getApi()->sendMessage($chatId, $this->getDecription(), 'html', null, false, $keyboard);
        }
    }

    public function make($api, $data)
    {
        return parent::make($api, $data); // TODO: Change the autogenerated stub
    }

    public function keyboard($chatId)
    {
        if (!TelegramDb::isAdmin($chatId)) {
            $keyboard = new \TelegramBot\Api\Types\Inline\InlineKeyboardMarkup(
                [
                    [
                        ['text' => 'Login', 'callback_data' => 'login'],
                    ]
                ]
            );
        } else {
            $keyboard = new \TelegramBot\Api\Types\Inline\InlineKeyboardMarkup(
                [
                    [
                        ['text' => 'Create Post', 'callback_data' => 'post-create'],
                        ['text' => 'Delete Post', 'callback_data' => 'post-delete'],
                        ['text' => 'User statistic', 'callback_data' => 'statistic'],
                    ]
                ]
            );
        }
        return $keyboard;
    }
}